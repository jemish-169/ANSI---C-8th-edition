/*Write a program that requests two float type numbers from the user and then divides the first
number by the second and display the result along with the numbers.
*/
#include <stdio.h>
int main()
{
    float a, b, s;
    printf("%f%d", &a, &b);
    s = a / b;;
    printf("division is %f", s);

    return 0;
}