/*
Write a program to read three integer values from the keyboard and displays the output stating
that they are the sides of righ-angsled triangle.
*/
#include <stdio.h>
int main()
{
    int a, b, c;
    scanf("%d%d%d", &a, &b, &c);
    printf("\t\t\t\t%d\n\n\n\n%d\t\t\t\t%d", a, b, c);
    return 0;
}